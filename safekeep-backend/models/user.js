const mongoose = require('mongoose');

const userSchema = new mongoose.Schema({
    username: {
        type: String,
        required: true,
        unique: true,
        trim: true
    },
    email: {
        type: String,
        required: true,
        unique: true,
        trim: true,
        lowercase: true
    },
    password: {
        type: String,
        required: true
    },
    role: {
        type: String,
        enum: ['BUSINESS_OWNER', 'CASHIER', 'INVENTORY_MANAGER'],
        required: true
    }
}, { timestamps: true });

module.exports = mongoose.model('User', userSchema);